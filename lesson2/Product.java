package lesson2;

public class Product {
    private int id;
    private String name;
    private String manufacturer_name;
    private int cost_price;


    public Product(int id, String name, String manufacturer_name, int cost_price) {
        this.id = id;
        this.name = name;
        this.manufacturer_name = manufacturer_name;
        this.cost_price = cost_price;
    }
}
